package com.aasfencoders.womensafety.Class;

public class ReceiveClass {

    private String name;
    private String number;

    public ReceiveClass(){

    }

    public ReceiveClass(String name , String number){
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

}
