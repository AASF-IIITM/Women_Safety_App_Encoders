package com.aasfencoders.womensafety.Class;

public class ContactNameClass {

    private String id;
    private String name;

    public ContactNameClass(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
