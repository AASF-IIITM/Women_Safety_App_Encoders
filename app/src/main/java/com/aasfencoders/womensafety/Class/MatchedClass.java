package com.aasfencoders.womensafety.Class;

public class MatchedClass {

    private String name;
    private String number;

    public MatchedClass(){

    }

    public MatchedClass(String name , String number){
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }

}
